public class kerdesek {
    int valasz;
    int pontszam;
    String kategoria;

    public kerdesek(int valasz, int pontszam, String kategoria) {
        this.valasz = valasz;
        this.pontszam = pontszam;
        this.kategoria = kategoria;
    }

    public int getValasz() {
        return valasz;
    }

    public int getPontszam() {
        return pontszam;
    }

    public String getKategoria() {
        return kategoria;
    }
    
    
    
}
